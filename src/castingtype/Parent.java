package castingtype;

public class Parent {
	     // type casting use to take screen shot,written the something in somewhere
	public void m1 () {
		System.out.println("m1 from parent");
	}
	public void m2 () {
		System.out.println("m2 from parent");
	}
	public void m2 (int i) {
		System.out.println("m2 from parent is :"+i);
	}
	
	public static void main(String[] args) {
		
		
		Parent p = new Parent();
	}

}
